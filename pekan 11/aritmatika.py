def penjumlahan(angka1,angka2):
    hasil = angka1+angka2
    print ("Hasil penjumlahan: ",hasil)
    
def pengurangaran(angka1,angka2):
    hasil = angka1-angka2
    print ("Hasil pengurangan: ",hasil)
    
def perkalian(angka1,angka2):
    hasil = angka1*angka2
    print ("Hasil perkalian: ",hasil)
    
def pembagian(angka1,angka2):
    hasil = angka1/angka2
    print ("Hasil pembagian: ", hasil)
    
def pangkat(angka1,angka2):
    hasil = angka1**angka2
    print ("Hasil pangkat: ", hasil)
    
def kuadrat(angka1):
    kuadrat = angka1**2
    print ("Hasil kuadrat: ", kuadrat)
    
def mod(angka1,angka2):
    hasil = angka1%angka2
    print ("Hasil mod: ", hasil)
    
def log (angka1,angka2):
    hasil = angka1**angka2
    print ("Hasil log: ", hasil)
    
def faktorial(angka1):
    faktorial = 1
    for i in range(1,angka1+1):
        faktorial = faktorial*i
    print ("Hasil faktorial: ", faktorial)
    
